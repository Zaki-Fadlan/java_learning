package org.example;

public class DataTypes {
    // Java Data Types diveded into two groups that is Primitive Java data types and  Non-Primitive Data types
    // Primitive Data Types : byte, short, int, long, double, boolean and char.
    // Number
    byte myByte;
    int myInteger;
    short myShort;
    long myLong;
    // Floating
    float myFloat;
    double myDouble;
    // Boolean
    boolean myBoolean;
    // Character
    char myChar;
    // Non-Primitive Data Types : String , Arrays, Classes.
    String myString;
    String[] myArray;


}

